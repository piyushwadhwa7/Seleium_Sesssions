package JavaBasis;

public class StringConcatination {
    public static void main(String[] args) {
        int a=190;
        int b=111;
        String X="HELLO";
        String y="haarry";

        System.out.println(a+b);
        System.out.println( X+y);
        System.out.println(a+X);
        System.out.println(a+b+X+y+a+b);
    }
}
