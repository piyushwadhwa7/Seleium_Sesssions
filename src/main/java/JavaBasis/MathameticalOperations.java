package JavaBasis;

public class MathameticalOperations {
    public static void main(String[] args) {
        System.out.println(10/2);//5
        System.out.println(9/2);//4.5--> 4 integer output will be integer
        System.out.println(9.0/2);//4.5


        System.out.println(9/0);//ArithmeticException: / by zero the program will get terminated here
        System.out.println(0/0.0);//NaN
    }
}
